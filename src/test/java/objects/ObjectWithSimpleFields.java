package objects;

public class ObjectWithSimpleFields {
    private int primitiveIntValue = 42;

    private Byte byteValue = 42;
    private Short shortValue = 42;
    private Integer integerValue = 42;
    private Long longValue = 42L;
    private Float floatValue = 42.0f;
    private Double doubleValue = 42.0;

    private Character characterValue = '2';
    private String stringValue = "42";

    private Boolean booleanValue = Boolean.TRUE;

    public int getPrimitiveIntValue() {
        return primitiveIntValue;
    }

    public void setPrimitiveIntValue(int primitiveIntValue) {
        this.primitiveIntValue = primitiveIntValue;
    }

    public Byte getByteValue() {
        return byteValue;
    }

    public void setByteValue(Byte byteValue) {
        this.byteValue = byteValue;
    }

    public Short getShortValue() {
        return shortValue;
    }

    public void setShortValue(Short shortValue) {
        this.shortValue = shortValue;
    }

    public Integer getIntegerValue() {
        return integerValue;
    }

    public void setIntegerValue(Integer integerValue) {
        this.integerValue = integerValue;
    }

    public Long getLongValue() {
        return longValue;
    }

    public void setLongValue(Long longValue) {
        this.longValue = longValue;
    }

    public Float getFloatValue() {
        return floatValue;
    }

    public void setFloatValue(Float floatValue) {
        this.floatValue = floatValue;
    }

    public Double getDoubleValue() {
        return doubleValue;
    }

    public void setDoubleValue(Double doubleValue) {
        this.doubleValue = doubleValue;
    }

    public Character getCharacterValue() {
        return characterValue;
    }

    public void setCharacterValue(Character characterValue) {
        this.characterValue = characterValue;
    }

    public String getStringValue() {
        return stringValue;
    }

    public void setStringValue(String stringValue) {
        this.stringValue = stringValue;
    }

    public Boolean getBooleanValue() {
        return booleanValue;
    }

    public void setBooleanValue(Boolean booleanValue) {
        this.booleanValue = booleanValue;
    }
}
